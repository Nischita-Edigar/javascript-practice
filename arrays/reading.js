//Reading elements of an array using for loop
let size=['xs','S','M','L','xl','xxl']
for(let i=0; i<=5; i=i+1){  //we can initialize by any value
    console.log(size[i])
}

//Reading elements of an array using using while loop
let discount=[49,59,99,199,299,399,499,599]
let i=0
while(i<=7){
    console.log(discount[i])
    i++
}

